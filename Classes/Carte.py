
class Carte:
    """Class that represents a playing card"""

    # Constructor and attributes
    def __init__(self, valeur):
        """Initializes a card with a value"""
        self.valeur = valeur

    # Methods
    def get_valeur(self):
        """Returns the value of the card"""
        return self.valeur
    
    def set_valeur(self, valeur):
        """Sets the value of the card"""
        self.valeur = valeur
    
